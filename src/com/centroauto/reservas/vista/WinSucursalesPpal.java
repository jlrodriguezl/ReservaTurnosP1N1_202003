    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        //Obtener la fila seleccionada en la tabla
        int row = this.tblDatos.getSelectedRow();
        if(row < 0){
            JOptionPane.showMessageDialog(this, "Debe seleccionar una sucursal",
                    "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try{
            //Obtener la informaci√≥n de la fila seleccionada
            SucursalesDTO s = 
                (SucursalesDTO)this.tblDatos.getValueAt(row, SucursalesModel.OBJECT_COL);
            WinSucursalesForm winSucursalesForm 
                    = new WinSucursalesForm(false, s, this);
            winSucursalesForm.setVisible(true);            
        }catch(Exception e){
           JOptionPane.showMessageDialog(this, e.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btnEditarActionPerformed

