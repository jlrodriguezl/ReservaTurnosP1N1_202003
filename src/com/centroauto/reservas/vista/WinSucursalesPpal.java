    public void llenarDepartamentos(){
        try{
            List<Departamentos> lstDeptos = deptosDao.consultarDepartamentos();
            this.cmbDeptos.addItem("0 - Seleccione");
            for(Departamentos d : lstDeptos){
                this.cmbDeptos.addItem(d.getIdDepto()+" - "+d.getNomDepto());
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Error: "+e.getMessage(),
                    "Error Consulta Deptos", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    public void llenarCiudades(){
        ciudadDao = new CiudadesDao();
        Integer idDepto = Integer.parseInt(this.cmbDeptos.getSelectedItem().toString()
                .split("-")[0].trim());
        try{
            this.cmbCiudades.removeAllItems();
            this.cmbCiudades.addItem("0 - Seleccione");
            if(idDepto > 0){
                List<Ciudades> lstCiudades = 
                    ciudadDao.consultarCiudadesPorDepto(idDepto);            
                for(Ciudades c : lstCiudades){
                    this.cmbCiudades.addItem(c.getIdCiudad()+" - "+c.getNomCiudad());
                }
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Error: "+e.getMessage(),
                    "Error Consulta Ciudades", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    public void consultarDatos(String nomSucursal, Integer idCiudad){
        try{            
            List<SucursalesDTO> lstSucursales;            
            lstSucursales = sucursalesDao.consultarSucursales(nomSucursal, idCiudad);
            SucursalesModel model = new SucursalesModel(lstSucursales);
            this.tblDatos.setModel(model);
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Error: "+e.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

